%% 基本概念
% 拉曼光谱中有两种常见的频率表示方法：
%
% 1. 绝对波数 (Absolute Wavenumber) [cm⁻¹]
%    = 1/λ，其中 λ 是光的波长 (cm)
%    范围通常很大：~1000-50000 cm⁻¹
%    例：488nm激发光 = 1/(488×10⁻⁷) = 20491.8 cm⁻¹
%
% 2. 相对拉曼位移 (Raman Shift) [cm⁻¹]
%    = 激发光波数 - 散射光波数 (对于斯托克斯拉曼)
%    = ν₀ - ν_scattered
%    范围较小：通常 0-3000 cm⁻¹
%    表示样品分子相对于激发光的振动能量

%% 实际数据的情况

% 情况1：数据中的轴是相对拉曼位移 (通常是这样)
% ────────────────────────────────────────
% 示例：486-528 cm⁻¹
% 说明：已经是相对于某个激发光的位移
% 设置：excitationWavelength_nm = 0  (禁用转换)
%      ramanPlotRange = [486 528]

% 情况2：数据中的轴是绝对波数 (较少见)
% ────────────────────────────────────────
% 示例：20000-21000 cm⁻¹ 的大数值
% 说明：是绝对波数，需要转换
% 设置：excitationWavelength_nm = 488  (激发光波长)
%      脚本会自动转换：
%        相对位移 = 20491.8 - 绝对波数

%% 常见激发光波长

% 激发光              波长       波数 [cm⁻¹]
% ────────────────────────────────────────
% 紫外              266 nm      37594 cm⁻¹
% 紫外              325 nm      30769 cm⁻¹
% 蓝光              405 nm      24691 cm⁻¹
% 蓝光              488 nm      20492 cm⁻¹   ← 您的激发光
% 绿光              514 nm      19461 cm⁻¹
% 红光              632 nm      15823 cm⁻¹
% 红外              785 nm      12739 cm⁻¹
% 红外              830 nm      12048 cm⁻¹

%% 如何设置

% 第1步：打开 power_dependence.m
% 第2步：找到第16-17行
% 第3步：根据您的情况设置：

% ─── 如果数据已经是相对位移 ───
excitationWavelength_nm = 0;      % 禁用转换
ramanPlotRange = [340 450];        % 使用实际的拉曼范围

% ─── 如果数据是绝对波数 ───
excitationWavelength_nm = 488;     % 488nm激发光
ramanPlotRange = [340 450];        % 期望的相对位移范围

% 脚本会自动：
% 1. 计算激发光波数：20491.8 cm⁻¹
% 2. 检测数据中的大数值（>1000）
% 3. 转换为相对位移：ν₀ - ν
% 4. 然后使用您指定的范围进行分析

%% 诊断方法

% 运行脚本后，查看输出：
% ────────────────────────────────────────
% 数据大小: 99 × 2000
% 功率范围: 0.1009 - 19.9673 mW
% 拉曼轴范围（原始）: 486 - 528 cm⁻¹
% ✓ 指定范围有效: [340 - 450 cm⁻¹]
% ────────────────────────────────────────
%
% 解释：
% - 原始范围 486-528 cm⁻¹ （较小数值）
%   → 已经是相对拉曼位移
% - 警告未出现
%   → 但范围 [340-450] 超出了 [486-528]
%   → 需要检查您的数据

%% 问题排查

% 问题1：显示 "超出实际数据范围" 警告
% ────────────────────────────────────────
% 原因：ramanPlotRange 超出了数据的实际范围
% 解决：
%   a) 检查原始数据的实际范围（脚本会显示）
%   b) 调整 ramanPlotRange 到有效范围内
%
% 示例：
%   如果数据范围是 486-528，但要查看 340-450：
%   → 这个范围不存在
%   → 需要重新检查数据来源

% 问题2：看到 "检测到可能是绝对波数"
% ────────────────────────────────────────
% 说明：脚本成功转换了数据
% 检查：转换后的范围是否符合预期

